<h2><strong>Giới thiệu chung về ngành </strong></h2>
<p><strong>Giáo dục học</strong> là ngành học trang bị kiến thức nền tảng về tâm lý giáo dục, quản lý giáo dục, giúp xây dựng và bồi dưỡng năng lực xây dựng kế hoạch, thiết kế, triển khai và đánh giá các hoạt động giáo dục, tư vấn, quản lý trường học, các cơ quan, cơ sở, trung tâm về giáo dục, các tổ chức phi chính phủ, đoàn thể xã hội có liên quan tới giáo dục&#8230;</p>
<p><strong>Chương trình đào tạo ngành Giáo dục học </strong>giúp sinh viên trang bị những kiến thức từ cơ bản tới chuyên sâu về giáo dục với các hướng chuyên môn như Tham vấn và công tác xã hội, Giảng dạy và tổ chức các hoạt động giáo dục, Đánh giá trong giáo dục, giảng dạy giáo dục học và các môn liên quan.</p>
<ul>
<li>Nhóm tham vấn học đường chú trọng đào tạo sinh viên theo hướng kiến thức và kỹ năng trong việc tổ chức nghiên cứu, triển khai quá trình tham vấn học đường cho các đối tượng có nhu cầu trong trường học.</li>
<li>Nhóm Đánh giá giáo dục tập trung vào việc trang bị kiến thức và kỹ năng về đánh giá lớp học, người học, người dạy và chương trình học tập.</li>
<li>Nhóm giảng dạy hướng tới việc trang bị cho sinh viên các kiến thức và kỹ năng để nghiên cứu và giảng dạy các học phần về giáo dục học và môn liên quan.</li>
</ul>
