<h2><strong>Cơ hội việc làm sau tốt nghiệp</strong></h2>
<p>Sinh viên hoàn thành chương trình học ngành Giáo dục học sẽ có đủ kiến thức nền tảng để đảm nhiệm nhiều vai trò công việc như nhân viên, chuyên viên, trợ lý, nhà tư vấn, nghiên cứu, giáo viên, quản lý &#8211; lãnh đạo trong lĩnh vực giáo dục thực tiễn.</p>
<p>Một số công việc các bạn sinh viên sau khi tốt nghiệp ngành Giáo dục học có thể tham khảo như:</p>
<ul>
<li>Chuyên viên tư vấn tâm lý trong các cơ sở giáo dục, hệ thống trường phổ thông</li>
<li>Tham vấn tâm lý tại các cơ sở, trung tâm tham vấn tư vấn tâm lý ngoài xã hội cho các đối tượng có nhu cầu</li>
<li>Cán bộ đánh giá giáo dục, chương trình giáo dục, đánh giá lớp học, học sinh, giáo viên&#8230;</li>
<li>Giảng dạy tại các cơ sở giáo dục</li>
<li>Giảng dạy về những lĩnh vực liên quan tới chuyên ngành giáo dục được đào tạo tại các cơ sở giáo dục chuyên biệt</li>
<li>Nghiên cứu và phát triển các dự án liên quan tới giáo dục</li>
<li>Tiếp tục học và nghiên cứu lên ở bậc thạc sĩ và tiến sĩ học giáo dục</li>
</ul>

 


